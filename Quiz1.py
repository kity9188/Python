# Write a small program that assigns the name of 2 trees to
# 2 variables, called tree1 and tree2.
# If the values of the 2nd variable are equal, print the message
# "The trees are same", otherwise print "The trees are different".

tree1 = 'Mango'
tree2 = 'Banana'

if (tree1 == tree2):
	print("The trees are the same.")
else:
	print("The trees are different")